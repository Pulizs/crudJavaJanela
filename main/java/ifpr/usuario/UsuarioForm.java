    public void editar(int id)
    {
        usuario = gerUsuario.buscarUsuario(id);
        nomeTF.setText(usuario.getNome());
        emailTF.setText(usuario.getEmail());
        
        this.converterLocalDateParaString();
        
        if ( usuario.getSexo() == Sexo.MASCULINO )
        {
            masculinoRB.setSelected(true);
        }
        else
        {
            femininoRB.setSelected(true);
        }
        
        senha1PF.setEnabled(false);
        senha2PF.setEnabled(false);
        
        salvarBT.setVisible(false);
        atualizarBT.setVisible(true);
    }
    
    private void converterLocalDateParaString()
    {
        DateTimeFormatter dtf =
            DateTimeFormatter.ofPattern("dd/MM/yyyy");
        String dtNasc =
            dtf.format(usuario.getDataNascimento());
        dataNascimentoTF.setText(dtNasc);
    }
    

