    private void salvarBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarBTActionPerformed
        String nome = nomeTF.getText().trim();
        String email = emailTF.getText().trim();
        String dtNasc = dataNascimentoTF.getText().trim();
        
        DateTimeFormatter dtf =
            DateTimeFormatter.ofPattern("dd/MM/yyyy");
        LocalDate dataNascimento =
            LocalDate.parse(dtNasc, dtf);
        
        String senha1 = senha1PF.getPassword().toString();
        String senha2 = senha2PF.getPassword().toString();
        
        boolean isValido = true;
        
        if ( nome.isEmpty() )
        {
            isValido = false;
            JOptionPane.showMessageDialog(this,
                    "Nome é obrigatório");
        }
        
        if ( nome.length() < 3 )
        {
            isValido = false;
            JOptionPane.showMessageDialog(this,
                    "Nome deve conter pelo menos 3 caracteres");
        }
        
        if ( email.isEmpty() )
        {
            isValido = false;
            JOptionPane.showMessageDialog(this,
                    "E-mail é obrigatório");
        }
        
        
        if ( isValido )
        {
            Usuario usuario = new Usuario();
            usuario.setNome(nome);
            usuario.setEmail(email);
            usuario.setDataNascimento(dataNascimento);

            if ( masculinoRB.isSelected() ) {
                usuario.setSexo(Sexo.MASCULINO);
            }
            else {
                usuario.setSexo(Sexo.FEMININO);
            }
            gerUsuario.salvar(usuario);
            this.limparTela();
            this.dispose();
        }
        
    }//GEN-LAST:event_salvarBTActionPerformed

